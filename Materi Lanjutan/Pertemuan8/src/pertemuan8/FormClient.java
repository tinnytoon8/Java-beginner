        try {
            // Minta input dari user
            String ip = JOptionPane.showInputDialog("Masukkan IP Server:", ""); // Ip bisa menggunakan 127.0.0.1 atau 0.0.0.0
            String portStr = JOptionPane.showInputDialog("Masukkan Port Server:", ""); // Nanti masukkan sesuai port di sisiServer atau npm
            String nama = JOptionPane.showInputDialog("Masukkan Nama Anda:");
            if (nama == null || nama.trim().isEmpty()) {
                nama = "User";  // Default nama
            }
            int port = Integer.parseInt(portStr);

            // Koneksi ke server
            s = new Socket(ip, port);
            din = new DataInputStream(s.getInputStream());
            dout = new DataOutputStream(s.getOutputStream());

            // Kirim nama ke server duluan
            dout.writeUTF("[NAMA]" + nama);  // Penanda nama

            // Terima pesan dari server
            String pesanmasuk = "";
            while (!pesanmasuk.equals("keluar")) {
                pesanmasuk = din.readUTF();
                isi_pesanclient.append("\nAdmin : " + pesanmasuk);
                isi_pesanclient.setCaretPosition(isi_pesanclient.getDocument().getLength());
            }

        } catch (Exception e) {
            isi_pesanclient.append("\n[ERROR] " + e.getMessage());
            isi_pesanclient.setCaretPosition(isi_pesanclient.getDocument().getLength());
        }
