    private void btn_kirimclientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_kirimclientActionPerformed
        // TODO add your handling code here:
        try {
            String msgout = txt_pesanclient.getText().trim();
            if (msgout.isEmpty() || dout == null) return;

            dout.writeUTF(msgout);  // Kirim ke server
            isi_pesanclient.append("\nMe: " + msgout);  // Tampilkan juga di sisi client
            isi_pesanclient.setCaretPosition(isi_pesanclient.getDocument().getLength());
            txt_pesanclient.setText(""); // Kosongkan field

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal mengirim pesan!\n" + e.getMessage());
        }
    }//GEN-LAST:event_btn_kirimclientActionPerformed

